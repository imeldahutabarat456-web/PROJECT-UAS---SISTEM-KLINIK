    private void jTableObatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableObatMouseClicked
        // TODO add your handling code here:
        int baris = jTableObat.getSelectedRow();
    
    if (baris != -1) {
        String id = jTableObat.getValueAt(baris, 0).toString();
        String nama = jTableObat.getValueAt(baris, 1).toString();
        String jenis = jTableObat.getValueAt(baris, 2).toString();
        String harga = jTableObat.getValueAt(baris, 3).toString();
  
        txtIdObat.setText(id);
        txtNamaObat.setText(nama);
        jComboBoxObat.setSelectedItem(jenis);
        txtHarga.setText(harga);
        txtIdObat.setEnabled(false); 
        }
    }//GEN-LAST:event_jTableObatMouseClicked

