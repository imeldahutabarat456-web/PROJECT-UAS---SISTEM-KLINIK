    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
     int konfirmasi = JOptionPane.showConfirmDialog(this, "Hapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if(konfirmasi == JOptionPane.YES_OPTION) {
            try {
                String sql = "DELETE FROM obat WHERE id_obat=?";
                PreparedStatement st = config.Koneksi.getKoneksi().prepareStatement(sql);
                st.setString(1, txtIdObat.getText());
                st.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data Dihapus!");
                tampilkanData();
                bersihForm();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal Hapus: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
      try {
            String sql = "INSERT INTO obat(id_obat, nama_obat, jenis_obat, harga) VALUES (?, ?, ?, ?)";
            Connection conn = config.Koneksi.getKoneksi();
            PreparedStatement st = conn.prepareStatement(sql);
            st.setString(1, txtIdObat.getText());
            st.setString(2, txtNamaObat.getText());
            st.setString(3, jComboBoxObat.getSelectedItem().toString());
            st.setString(4, txtHarga.getText());
            st.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan!");
            tampilkanData();
            bersihForm();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal Simpan: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

