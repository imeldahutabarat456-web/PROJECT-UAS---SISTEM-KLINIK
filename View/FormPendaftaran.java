    public void tampilkanData() {
      DefaultTableModel model = (DefaultTableModel) jTableDaftar.getModel();
    model.setRowCount(0);
 
    String sql = "SELECT no_daftar, id_pasien, id_dokter, tanggal, keluhan FROM pendaftaran";
    
    try {
        Connection conn = config.Koneksi.getKoneksi();
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(sql);
        while(rs.next()){
            model.addRow(new Object[]{
                rs.getString("no_daftar"),
                rs.getString("tanggal"),
                rs.getString("id_pasien"), 
                rs.getString("id_dokter"), 
                rs.getString("keluhan")
            });
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage());
    }
    }

    private void loadPasien() {
        cmbPasien.removeAllItems();
        cmbPasien.addItem("- Pilih Pasien -");
        try {
            Connection conn = config.Koneksi.getKoneksi();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT id_pasien FROM pasien");
            while(rs.next()) {
                cmbPasien.addItem(rs.getString("id_pasien"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data pasien: " + e.getMessage());
        }
    }

    private void loadDokter() {
        cmbDokter.removeAllItems();
        cmbDokter.addItem("- Pilih Dokter -");
        try {
            Connection conn = config.Koneksi.getKoneksi();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT id_dokter FROM dokter");
            while(rs.next()) {
                cmbDokter.addItem(rs.getString("id_dokter"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data dokter: " + e.getMessage());
        }
    }

