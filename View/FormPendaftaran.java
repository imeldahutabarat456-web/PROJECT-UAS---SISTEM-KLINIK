    private void btnDaftarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDaftarActionPerformed
        if (txtNoDaftar.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "No Pendaftaran tidak boleh kosong!");
        return;
    }
   
    
    try {
        Connection conn = config.Koneksi.getKoneksi();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String formatTanggal = sdf.format(jCalendar1.getDate());
        
        String sqlCek = "SELECT COUNT(*) FROM pendaftaran WHERE no_daftar = ?";
        PreparedStatement psCek = conn.prepareStatement(sqlCek);
        psCek.setString(1, txtNoDaftar.getText());
        ResultSet rsCek = psCek.executeQuery();
        rsCek.next();
        
        if (rsCek.getInt(1) > 0) {
            String sqlUpdate = "UPDATE pendaftaran SET tgl_daftar=?, id_pasien=?, id_dokter=?, keluhan=? WHERE no_daftar=?";
            PreparedStatement st = conn.prepareStatement(sqlUpdate);
            st.setString(1, cmbPasien.getSelectedItem().toString());
            st.setString(2, cmbDokter.getSelectedItem().toString());
            st.setString(3, formatTanggal);
            st.setString(4, txtKeluhan.getText());
            st.setString(5, txtNoDaftar.getText());
            st.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data Pendaftaran Berhasil Diperbarui!");
        } else {
            String sqlInsert = "INSERT INTO pendaftaran ( no_daftar, id_pasien, id_dokter, tanggal, keluhan) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement st = conn.prepareStatement(sqlInsert);
            st.setString(1, txtNoDaftar.getText());
            st.setString(2, cmbPasien.getSelectedItem().toString());
            st.setString(3, cmbDokter.getSelectedItem().toString());
            st.setString(4, formatTanggal);
            st.setString(5, txtKeluhan.getText());
            st.executeUpdate();
            JOptionPane.showMessageDialog(this, "Pendaftaran Baru Berhasil Disimpan!");
        }
        
        tampilkanData();
        bersihForm();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal memproses data: " + e.getMessage());
    }
    }//GEN-LAST:event_btnDaftarActionPerformed

