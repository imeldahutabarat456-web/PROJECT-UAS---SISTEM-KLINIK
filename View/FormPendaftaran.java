    private void jTableDaftarMouseClicked(java.awt.event.MouseEvent evt) {
        int baris = jTableDaftar.rowAtPoint(evt.getPoint());
        if (baris != -1) {
            txtNoDaftar.setText(jTableDaftar.getValueAt(baris, 0).toString());
            try {
                String tglDariTabel = jTableDaftar.getValueAt(baris, 1).toString();
                Date date = new SimpleDateFormat("yyyy-MM-dd").parse(tglDariTabel);
                jCalendar1.setDate(date);
            } catch (Exception e) {
                setTanggalHariIni();
            }
            
            cmbPasien.setSelectedItem(jTableDaftar.getValueAt(baris, 2).toString());
            cmbDokter.setSelectedItem(jTableDaftar.getValueAt(baris, 3).toString());
            txtKeluhan.setText(jTableDaftar.getValueAt(baris, 4) != null ? jTableDaftar.getValueAt(baris, 4).toString() : "");
        }
    }
    private void txtCariKeyReleased(java.awt.event.KeyEvent evt) {
        halamanSekarang = 1;
        tampilkanData();
    }

