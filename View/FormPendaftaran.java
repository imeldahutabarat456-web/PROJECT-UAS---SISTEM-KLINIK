    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
      if (txtNoDaftar.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Pilih data pendaftaran pada tabel yang ingin dihapus!");
            return;
        }
        
        int konfirmasi = JOptionPane.showConfirmDialog(this, "Hapus transaksi pendaftaran ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
        if(konfirmasi == JOptionPane.YES_OPTION) {
            try {
                String sql = "DELETE FROM pendaftaran WHERE no_daftar=?";
                PreparedStatement st = config.Koneksi.getKoneksi().prepareStatement(sql);
                st.setString(1, txtNoDaftar.getText());
                st.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data Pendaftaran Berhasil Dihapus!");
                tampilkanData();
                bersihForm();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal Menghapus: " + e.getMessage());
            }
        } 
    }//GEN-LAST:event_btnHapusActionPerformed
